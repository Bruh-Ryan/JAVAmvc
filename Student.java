//model
 
class StudentModel
{
    private String name;
    private String rollno;
    private int marks;

    public void setName(String name){
        this.name=name;
    }

    public String getName()
    {
        return name;
    }

    public String getRollno() {
        return rollno;
    }

    public void setRollno(String rollno) {
        this.rollno = rollno;
    }

    public int getMarks() {
        return marks;
    }

    public void setMarks(int marks) {
        this.marks = marks;
    }

    

}